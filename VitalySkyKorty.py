# range(START=4, STOP=-16-1, STEP=-2)
for i in range(4, -16, -2):
    print(i, end=' ')  # 4 2 0 -2 -4 -6 -8 -10 -12 -14
print()

# range(START=3, STOP=13-1, STEP=2)
