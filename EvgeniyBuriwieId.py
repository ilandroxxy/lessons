M = [2 ** x for x in range(30)]
for n in range(1, 10000):
    s = ">" + "0" * 17 + "3" * n + "2" * 17
    while ">3" in s or ">2" in s or ">0" in s:
        if ">3" in s:
            s = s.replace(">3", "22>", 1)
        if ">2" in s:
            s = s.replace(">2", "2>", 1)
        if ">0" in s:
            s = s.replace(">0", "3>", 1)
    summa = sum([int(x) for x in s if x.isdigit()])
    if summa in M:
        print(n)

