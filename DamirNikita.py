# № 12462 PRO100 ЕГЭ 29.12.23 (Уровень: Базовый)
# Сколько существует различных трёхзначных и пятизначных чисел,
# записанных в шестнадцатеричной системе счисления, в записи которых
# цифры следуют слева направо в строго убывающем порядке?

cnt = 0
from itertools import permutations

for p in permutations('0123456789ABCDEF', r=3):
    num = ''.join(p)
    if num[0] != '0':
        if list(num) == sorted(num)[::-1]:
            cnt += 1

for p in permutations('0123456789ABCDEF', r=5):
    num = ''.join(p)
    if num[0] != '0':
        if list(num) == sorted(num)[::-1]:
            cnt += 1
print(cnt)

