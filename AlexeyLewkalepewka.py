def F(a, b):
    if a < b or a == 16:
        return 0
    if a == b:
        return 1
    else:
        if a % 3 == 0:
            return F(a-2, b) + F(a // 3, b)
        else:
            return F(a-2, b) + F(a - 4, b)


print(F(36, 4))

