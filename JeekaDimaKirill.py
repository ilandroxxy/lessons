# Px – среднее арифметическое абсцисс центров кластеров, и
# Py – среднее арифметическое ординат центров кластеров.
# В ответе запишите четыре числа: в первой строке сначала целая часть
# абсолютного значения произведения
# Px × 10000, затем целая часть абсолютного значения произведения
# Py × 10000 для файла А, во второй строке – аналогичные данные для файла B.

print(center(clustersA[0]))  # [3.7046261187, 5.757797218]
print(center(clustersA[1]))  # [2.0161619849, -3.6989345413]
PxA = (3.7046261187 + 2.0161619849) / 2 * 10000
