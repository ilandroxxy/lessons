def center(cl):
    R = []
    for p in cl:
        summa = 0
        for g in cl:
            summa += dist(p, g)
        # print(summa, p)
        R.append([summa, p])
    return min(R)[1]

# Для каждого файла определите координаты центра каждого кластера
