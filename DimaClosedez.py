def convert(n, b):
    r = ''
    while n > 0:
        r += str(n % b)
        n //= b
    return r[::-1]

z = []
for n in range(1, 10000):
    s = convert(n, 4)

    if len(s) % 2 == 0:
        s = s[:len(s)//2] + '0' + s[len(s)//2:]

    r = int(s, 4)
    if r <= 180:
        z.append(r)
print(max(z))

