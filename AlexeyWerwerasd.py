def convert(n, b):
    s = ''
    while n > 0:
        s += str(n % b)
        n //= b
    return s[::-1]

for n in range(1, 1000):
    s = convert(n, 5)
    if n % 25 == 0:
        s = s[-3:] + s
    else:
        x = n % 5
        s = s + convert(x, 5)
    r = int(s, 5)
    if r > 10000:
        print(n)
        break


