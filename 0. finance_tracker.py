"""
╔══════════════════════════════════════════════════════╗
║         ФИНАНСОВЫЙ ТРЕКЕР — РЕПЕТИТОРСТВО            ║
║         Еженедельный учёт доходов и расходов         ║
╚══════════════════════════════════════════════════════╝

Формат полей
------------
Все поля принимают как простые числа, так и кортежи (сумма, "описание"):

    d=[20000, 5693]                       # без описаний
    d=[(20000, "ТКС авто"), (5693, "Ozon рассрочка")]  # с описаниями

z  — доход от занятий
g  — выплаты команде (5 помощников, раз в месяц)
d  — долги и обязательные платежи по кредитам
p  — подписки, интернет и прочие сервисы
e  — ежедневные траты: (сумма, "описание")
received — поступившие оплаты от учеников: (сумма, "Имя (@username)")
"""

# region ── Утилиты ──────────────────────────────────────────────────────────

W = 60  # ширина блока


def rub(n, plus: bool = False) -> str:
    value = round(n)
    s = f"{abs(value):,}".replace(",", "\u2009")
    if value < 0:
        return f"\u2212{s} \u20bd"
    if plus and value > 0:
        return f"+{s} \u20bd"
    return f"{s} \u20bd"


def _line(char: str = "\u2550") -> str:
    return char * W


def _row(label: str, value: float, indent: int = 0, plus: bool = False) -> None:
    pad = "  " * indent
    full_label = f"{pad}{label}"
    val_str = rub(value, plus=plus)
    padding = W - len(full_label) - len(val_str)
    print(full_label + " " * max(padding, 1) + val_str)


def _parse(items: list) -> list[tuple[float, str | None]]:
    """Приводит list[number | (number, str)] к единому формату [(float, label|None)]."""
    result = []
    for item in items:
        if isinstance(item, (int, float)):
            result.append((float(item), None))
        else:
            result.append((float(item[0]), str(item[1]) if item[1] else None))
    return result


def _has_labels(parsed: list) -> bool:
    return any(lbl for _, lbl in parsed)


def _print_section(title: str, parsed: list, indent: int = 1) -> float:
    """Печатает строку раздела и детализацию (если есть метки). Возвращает итог."""
    total = round(sum(a for a, _ in parsed))
    if not total:
        return 0.0
    _row(title, -total, indent=indent)
    if _has_labels(parsed):
        for amt, lbl in parsed:
            if amt and lbl:
                _row(lbl, -amt, indent=indent + 1)
    return float(total)

# endregion


# region ── Функция отчёта за неделю ────────────────────────────────────────

def week_report(
    label: str,
    *,
    z: list,
    g: list | None = None,
    d: list | None = None,
    p: list | None = None,
    e: list | None = None,
    received: list | None = None,
) -> dict:
    """
    Параметры
    ---------
    label    : заголовок недели, например "24.05.26"
    z        : суммы за занятия
    g        : выплаты команде — number или (сумма, "описание")
    d        : долги / кредиты — number или (сумма, "описание")
    p        : подписки        — number или (сумма, "описание")
    e        : ежедневные траты — (сумма, "описание")
    received : оплаты учеников — (сумма, "Имя (@username)")
               сумма = 0 означает, что ученик ещё не оплатил
    """
    income = sum(z)

    g_parsed = _parse(g) if g else []
    d_parsed = _parse(d) if d else []
    p_parsed = _parse(p) if p else []
    e_parsed = _parse(e) if e else []

    team  = round(sum(a for a, _ in g_parsed))
    debts = sum(a for a, _ in d_parsed)
    subs  = sum(a for a, _ in p_parsed)
    daily = sum(a for a, _ in e_parsed)

    expenses = team + debts + subs + daily
    balance  = income - expenses

    # ── Заголовок
    print(f"\n{_line()}")
    print(f"  Неделя {label}")
    print(_line("\u2500"))

    # ── Доход
    _row("Доход от занятий", income)

    # ── Обязательные расходы
    has_mandatory = team or debts or subs
    if has_mandatory:
        print(_line("\u2500"))
        if g_parsed: _print_section("Команда",         g_parsed)
        if d_parsed: _print_section("Кредиты / долги", d_parsed)
        if p_parsed: _print_section("Подписки",        p_parsed)
        print(_line("\u2500"))
        _row("Итого обязательных", -(team + debts + subs))

    # ── Ежедневные траты
    if e_parsed:
        print(_line("\u2500"))
        for amt, desc in e_parsed:
            _row(desc or "трата", -amt, indent=1)
        print(_line("\u2500"))
        _row("Итого трат", -daily)

    # ── Остаток
    print(_line("\u2500"))
    _row("Остаток", balance, plus=True)

    # ── Оплаты учеников
    if received is not None:
        r_parsed = _parse(received)
        paid   = [(a, l) for a, l in r_parsed if a > 0]
        unpaid = [(a, l) for a, l in r_parsed if a == 0]
        got      = sum(a for a, _ in paid)
        expected = income - got

        print(_line("\u2500"))

        if paid:
            print(f"  Оплатили ({len(paid)} чел.):")
            for amt, lbl in paid:
                _row(lbl or "—", amt, indent=2)

        if unpaid:
            names = ", ".join(
                (l.split(" (")[0] if l else "?") for _, l in unpaid
            )
            print(f"  Не оплатили ({len(unpaid)} чел.): {names}")

        print(_line("\u2500"))
        _row("Получено",  got)
        _row("Ожидается", expected)

    # ── Доп. статистика
    if expenses and income:
        ratio  = expenses / income
        filled = round(ratio * 44)
        empty  = 44 - filled
        print(_line("\u2500"))
        print(f"  Расходы: {ratio * 100:.1f}% от дохода")
        print(f"  [{'\u2588' * filled}{'\u2591' * empty}]")
        for name, val in [
            ("команда",  team),
            ("кредиты",  debts),
            ("подписки", subs),
            ("траты",    daily),
        ]:
            if val:
                print(f"    {name:<12} {val / income * 100:>5.1f}%")

    print(_line())

    return {
        "label": label, "income": income,
        "team": team, "debts": debts, "subs": subs, "daily": daily,
        "expenses": expenses, "balance": balance,
    }

